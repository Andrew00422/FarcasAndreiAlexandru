using System;

namespace ConsoleApp
{
    public static class DataType
    {
        public static void Display()
        {
            Console.WriteLine("Data Types in C#:");
            Console.WriteLine($"int: {60}");
            Console.WriteLine($"double: {4.32}");
            Console.WriteLine($"char: {'Z'}");
            Console.WriteLine($"string: {"Good evening!"}");
            Console.WriteLine($"bool: {true}");
        }
    }
}